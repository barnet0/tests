package com.digiwin.ecims.platforms.yunji.bean.domain.logistic;

public class LogisticList {
	private String skuNo;
	private String logisticsCompanyNo;
	private String logisticsNumber;
	private String qty;
	private String[] uniqNos;
	public String getSkuNo() {
		return skuNo;
	}
	public void setSkuNo(String skuNo) {
		this.skuNo = skuNo;
	}
	public String getLogisticsCompanyNo() {
		return logisticsCompanyNo;
	}
	public void setLogisticsCompanyNo(String logisticsCompanyNo) {
		this.logisticsCompanyNo = logisticsCompanyNo;
	}
	public String getLogisticsNumber() {
		return logisticsNumber;
	}
	public void setLogisticsNumber(String logisticsNumber) {
		this.logisticsNumber = logisticsNumber;
	}
	public String getQty() {
		return qty;
	}
	public void setQty(String qty) {
		this.qty = qty;
	}
	public String[] getUniqNos() {
		return uniqNos;
	}
	public void setUniqNos(String[] uniqNos) {
		this.uniqNos = uniqNos;
	}

	
}
