package com.digiwin.ecims.platforms.baidu.bean.domain.item;

public class UserDefinedPropertyValue {

  private Integer pid;

  private Integer isUserDefined;

  private String pname;

  private String vname;

  public Integer getPid() {
    return pid;
  }

  public void setPid(Integer pid) {
    this.pid = pid;
  }

  public Integer getIsUserDefined() {
    return isUserDefined;
  }

  public void setIsUserDefined(Integer isUserDefined) {
    this.isUserDefined = isUserDefined;
  }

  public String getPname() {
    return pname;
  }

  public void setPname(String pname) {
    this.pname = pname;
  }

  public String getVname() {
    return vname;
  }

  public void setVname(String vname) {
    this.vname = vname;
  }

}
